ALTER TABLE public.intra_warehouse_transfers ENABLE ROW LEVEL SECURITY;

CREATE POLICY "intra_transfers_select"
  ON public.intra_warehouse_transfers FOR SELECT TO authenticated
  USING (
    (auth.jwt() -> 'user_metadata' ->> 'role') IN (
      'ADMIN', 'WAREHOUSE_MANAGER', 'FINANCE_MANAGER'
    )
    OR
    (
      (auth.jwt() -> 'user_metadata' ->> 'role') IN ('BU_MANAGER', 'UNIT_STAFF')
      AND to_sbu_id::text = (auth.jwt() -> 'user_metadata' ->> 'sbu_id')
    )
  );

CREATE POLICY "intra_transfers_insert"
  ON public.intra_warehouse_transfers FOR INSERT TO authenticated
  WITH CHECK (
    (auth.jwt() -> 'user_metadata' ->> 'role') IN ('ADMIN', 'WAREHOUSE_MANAGER')
  );

COMMIT;
